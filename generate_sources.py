"""Auto-generate catalog dbt raw models from datasources.yml.

Generated files:
  - models/main/raw/raw_{name}_manifest.sql (per datasource)
  - models/main/raw/raw_{name}_catalog.sql (per datasource)
  - models/main/raw/raw_{name}_meta.sql (per datasource)
  - models/main/raw/_schema.yml
  - models/main/stg/stg_all_manifests.sql (UNION ALL)
  - models/main/stg/stg_all_catalogs.sql (UNION ALL)
  - models/main/stg/stg_all_metas.sql (UNION ALL)

Also generates:
  - .fdl/artifacts/{name}_meta.json (fdl.toml → JSON conversion)
"""

from __future__ import annotations

import json
import os
import sys
import tomllib
from pathlib import Path

import yaml

CATALOG_DIR = Path(__file__).resolve().parent
DATASOURCES_YML = CATALOG_DIR / "datasources.yml"
RAW_DIR = CATALOG_DIR / "models" / "main" / "raw"
STG_DIR = CATALOG_DIR / "models" / "main" / "stg"
ARTIFACTS_DIR = CATALOG_DIR / ".fdl" / "artifacts"

GENERATED_HEADER = "-- Auto-generated by generate_sources.py. Do not edit manually."


def load_datasources() -> list[dict]:
    with open(DATASOURCES_YML) as f:
        return yaml.safe_load(f)["datasources"]


def detect_storage_base() -> tuple[str, str, bool]:
    """Detect the storage base where each dataset's meta.json lives.

    Uses public HTTPS so that dbt running on MotherDuck Pulse can read
    `<base>/<dataset>/dbt/catalog.json` directly via httpfs (no S3 secret
    needed). meta.json fetch in `generate_meta_jsons` uses the same HTTPS URL.

    Returns (base_url, target_name, is_s3) — is_s3 is always False now.
    """
    if os.environ.get("QUERIA_S3_BUCKET"):
        public = os.environ.get("QUERIA_PUBLIC_URL", "https://data.queria.io")
        return public.rstrip("/"), "default", False

    dev_pub = os.environ.get(
        "QUERIA_DEV_PUBLIC_URL", "https://pub-0292714ad4094bd0aaf8d36835b0972a.r2.dev"
    )
    legacy = os.environ.get("FDL_DATA_URL", "")
    if legacy.startswith("s3://"):
        return legacy.rstrip("/"), "default", True
    if legacy:
        base = str(Path(legacy).parent.parent)
        return base, "local", False
    return dev_pub.rstrip("/"), "local", False


def s3_get(client, bucket: str, key: str) -> bytes | None:
    try:
        resp = client.get_object(Bucket=bucket, Key=key)
        return resp["Body"].read()
    except client.exceptions.NoSuchKey:
        return None


def generate_meta_jsons(
    datasources: list[dict], base_url: str, target_name: str, is_s3: bool
) -> None:
    """Fetch each dataset's meta.json from R2 (or local storage).

    Each dataset publishes its own meta.json via dataset-shared's
    upload_artifacts.py — built from pyproject.toml [tool.queria].

    During the fdl → MotherDuck transition, datasets that haven't migrated
    yet still publish fdl.toml only. We fall back to reading fdl.toml and
    convert it to the meta.json shape inline so the catalog keeps building.

    base_url may be:
    - https:// public URL (preferred, no auth)
    - s3:// (legacy, needs boto3)
    - local path
    """
    import urllib.error
    import urllib.request

    ARTIFACTS_DIR.mkdir(parents=True, exist_ok=True)

    s3_client = None
    s3_bucket = None
    if is_s3:
        import boto3
        s3_client = boto3.client(
            "s3",
            endpoint_url=os.environ.get("QUERIA_S3_ENDPOINT") or os.environ["FDL_S3_ENDPOINT"],
            aws_access_key_id=os.environ.get("QUERIA_S3_ACCESS_KEY_ID")
            or os.environ["FDL_S3_ACCESS_KEY_ID"],
            aws_secret_access_key=os.environ.get("QUERIA_S3_SECRET_ACCESS_KEY")
            or os.environ["FDL_S3_SECRET_ACCESS_KEY"],
        )
        s3_bucket = base_url.replace("s3://", "")

    is_http = base_url.startswith(("http://", "https://"))

    def _http_get(url: str) -> bytes | None:
        try:
            req = urllib.request.Request(url, headers={"User-Agent": "curl/8.0"})
            with urllib.request.urlopen(req) as resp:
                return resp.read()
        except urllib.error.HTTPError:
            return None

    public_url = base_url if is_http else os.environ.get(
        "QUERIA_PUBLIC_URL", "https://data.queria.io"
    )

    for ds in datasources:
        name = ds["name"]

        # First try the new format (meta.json published by dataset-shared upload_artifacts.py)
        data: str | None = None
        if is_http:
            raw = _http_get(f"{base_url}/{name}/meta.json")
            if raw:
                data = raw.decode()
        elif is_s3:
            raw = s3_get(s3_client, s3_bucket, f"{name}/meta.json")
            if raw:
                data = raw.decode()
        else:
            meta_path = Path(base_url) / name / "meta.json"
            if meta_path.exists():
                data = meta_path.read_text()

        # Fall back to legacy fdl.toml if meta.json hasn't been published yet.
        if data is None:
            data = _legacy_fdl_toml_to_meta(
                name, base_url, public_url, is_s3, is_http, s3_client, s3_bucket
            )

        if data is None:
            print(f"  {name}: no meta.json or fdl.toml found, skipping")
            continue

        out_path = ARTIFACTS_DIR / f"{name}_meta.json"
        out_path.write_text(data)


def _legacy_fdl_toml_to_meta(
    name: str,
    base_url: str,
    public_url: str,
    is_s3: bool,
    is_http: bool,
    s3_client,
    s3_bucket: str | None,
) -> str | None:
    """Convert legacy fdl.toml to the meta.json layout. Returns JSON string or None."""
    import urllib.error
    import urllib.request

    config: dict
    if is_http:
        try:
            req = urllib.request.Request(
                f"{base_url}/{name}/fdl.toml", headers={"User-Agent": "curl/8.0"}
            )
            with urllib.request.urlopen(req) as resp:
                config = tomllib.loads(resp.read().decode())
        except urllib.error.HTTPError:
            return None
    elif is_s3:
        raw = s3_get(s3_client, s3_bucket, f"{name}/fdl.toml")
        if not raw:
            return None
        config = tomllib.loads(raw.decode())
    else:
        toml_path = Path(base_url) / name / "fdl.toml"
        if not toml_path.exists():
            return None
        with open(toml_path, "rb") as f:
            config = tomllib.load(f)

    meta = config.get("meta", {})
    out = {
        "datasource": name,
        "title": meta.get("title", ""),
        "description": meta.get("description", ""),
        "cover": meta.get("cover", ""),
        "tags": meta.get("tags", []),
        "repository_url": meta.get("repository_url", ""),
        "schedule": meta.get("schedule", ""),
        "license": meta.get("license", ""),
        "license_url": meta.get("license_url", ""),
        "source_url": meta.get("source_url", ""),
        "ducklake_url": f"{public_url}/{name}/ducklake.duckdb",
        "schemas": meta.get("schemas", {}),
    }
    return json.dumps(out, ensure_ascii=False, indent=2)


def generate_raw_sql(name: str, macro: str) -> str:
    return f"{GENERATED_HEADER}\n{{{{ {macro}('{name}') }}}}\n"


def generate_union_all(datasources: list[dict], suffix: str) -> str:
    lines = [GENERATED_HEADER, "{{ config(materialized='view') }}", ""]
    for i, ds in enumerate(datasources):
        if i > 0:
            lines.append("UNION ALL")
        lines.append(f"SELECT * FROM {{{{ ref('raw_{ds['name']}_{suffix}') }}}}")
    lines.append("")
    return "\n".join(lines)


def generate_schema_yml(datasources: list[dict]) -> str:
    models = []
    for ds in datasources:
        for suffix, label in [
            ("manifest", "manifest"),
            ("catalog", "catalog"),
            ("meta", "メタデータ"),
        ]:
            models.append({
                "name": f"raw_{ds['name']}_{suffix}",
                "description": f"{ds['description']} ({label})",
            })
    schema = {"version": 2, "models": models}
    header = "# Auto-generated by generate_sources.py. Do not edit manually.\n"
    return header + yaml.dump(schema, default_flow_style=False, allow_unicode=True, sort_keys=False)


def warn_stale_files(datasources: list[dict]) -> None:
    expected = {"_schema.yml"}
    for ds in datasources:
        for suffix in ["manifest", "catalog", "meta"]:
            expected.add(f"raw_{ds['name']}_{suffix}.sql")
    for path in RAW_DIR.iterdir():
        if path.name.startswith("."):
            continue
        if path.name not in expected:
            print(f"WARNING: stale file found: {path.relative_to(CATALOG_DIR)}", file=sys.stderr)


def main() -> None:
    datasources = load_datasources()
    RAW_DIR.mkdir(parents=True, exist_ok=True)
    STG_DIR.mkdir(parents=True, exist_ok=True)

    base_url, target_name, is_s3 = detect_storage_base()

    # Set FDL_STORAGE_BASE for dbt macros (read_dataset_manifest/catalog)
    os.environ["FDL_STORAGE_BASE"] = base_url

    # Convert fdl.toml → JSON (generates .fdl/artifacts/{name}_meta.json)
    generate_meta_jsons(datasources, base_url, target_name, is_s3)

    # Generate per-datasource raw models
    for ds in datasources:
        name = ds["name"]
        for suffix, macro in [
            ("manifest", "read_dataset_manifest"),
            ("catalog", "read_dataset_catalog"),
            ("meta", "read_dataset_meta"),
        ]:
            out = RAW_DIR / f"raw_{name}_{suffix}.sql"
            out.write_text(generate_raw_sql(name, macro))
            print(f"  generated: {out.relative_to(CATALOG_DIR)}")

    # Generate UNION ALL stg models
    for suffix in ["manifest", "catalog", "meta"]:
        path = STG_DIR / f"stg_all_{suffix}s.sql"
        path.write_text(generate_union_all(datasources, suffix))
        print(f"  generated: {path.relative_to(CATALOG_DIR)}")

    # Schema
    schema_path = RAW_DIR / "_schema.yml"
    schema_path.write_text(generate_schema_yml(datasources))
    print(f"  generated: {schema_path.relative_to(CATALOG_DIR)}")

    warn_stale_files(datasources)
    print("done.")


if __name__ == "__main__":
    main()
